list = [[]] * 5  # [[], [], [], [], []]  multipy 5 times of each element
list[0].append(10) # [[10],[10],[10],[10],[10]]
list[1].append(20) # [[10, 20], [10, 20], [10, 20], [10, 20], [10, 20]]
list[2].append(30) 
		# [[10, 20, 30], [10, 20, 30], [10, 20, 30], [10, 20, 30], [10, 20, 30]]
del list[3][2]	# [[10, 20], [10, 20], [10, 20], [10, 20], [10, 20]]
list.append(30)	# [[10, 20], [10, 20], [10, 20], [10, 20], [10, 20], 30]


